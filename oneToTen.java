public class oneToTen {
    public static void main(String[] args) {
        for (int counter = 0; counter < 11; counter++) {
            System.out.println(counter);    //print in different line
        }
        for (int counter = 0; counter < 11; counter++) {
            System.out.print(counter+" ");  //print in same line
        }
    }
}
